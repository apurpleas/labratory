def area(a: float) -> float:
    """
    Calculates the area of the square
    :param a: side of the square
    :returns area
    """
    return a * a


def perimeter(a: float) -> float:
    """
    Calculates the perimeter of the square
    :param a: side of the square
    :returns perimeter
    """
    return 4 * a
